package com.hyunhye.board.model;

public class CategoryModel {
	int CID;
	String ITEM;

	public CategoryModel() {
	}
	public int getCID() {
		return CID;
	}

	public void setCID(int cID) {
		CID = cID;
	}
	public String getITEM() {
		return ITEM;
	}
	public void setITEM(String iTEM) {
		ITEM = iTEM;
	}
}
